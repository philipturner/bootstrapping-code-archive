    let direction = SIMD3<Float>(1, 1, 1) / Float(3).squareRoot()
    let component = (atom.position * direction).sum()
    if component < 1.8 {
      // Explode the atoms from the centerpoint.
      let centerPoint = SIMD3<Float>(1.15, 0.70, 1.15)
      let siliconConstant = Constant(.square) { .elemental(.silicon) }
      let siliconCarbideConstant = Constant(.square) {
        .checkerboard(.silicon, .carbon)
      }
      
      var delta = atom.position - centerPoint
      delta *= siliconConstant / siliconCarbideConstant
      atom.position = centerPoint + delta
    }
    topology.atoms[atomID] = atom
  }
  
  // Add the hydrogen passivators.
  do {
    let orbitals = topology.nonbondingOrbitals()
    
    var insertedAtoms: [Entity] = []
    var insertedBonds: [SIMD2<UInt32>] = []
    for atomID in topology.atoms.indices {
      let atom = topology.atoms[atomID]
      let element = Element(rawValue: atom.atomicNumber)!
      
      for orbital in orbitals[atomID] {
        let bondLength =
        element.covalentRadius + Element.hydrogen.covalentRadius
        let hydrogenPosition = atom.position + bondLength * orbital
        let hydrogen = Entity(
          position: hydrogenPosition, type: .atom(.hydrogen))
        let hydrogenID = topology.atoms.count + insertedAtoms.count
        
        let bond = SIMD2(UInt32(atomID), UInt32(hydrogenID))
        insertedAtoms.append(hydrogen)
        insertedBonds.append(bond)
      }
    }
    topology.insert(atoms: insertedAtoms)
    topology.insert(bonds: insertedBonds)
  }
  
  topology.atoms = tooltipMinimizedStructure_Full
  
  return topology.atoms
}
